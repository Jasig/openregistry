  `username` varchar(50) NOT NULL,
  `password` varchar(50) default 'NOT_USED',
  `description` varchar(50) default NULL,
  `enabled` tinyint(4) NOT NULL default '1',
  PRIMARY KEY  (`username`)
) ENGINE=InnoDB DEFAULT CHARSET=latin1;

