public class AuxiliaryProgramException extends Exception{

    public AuxiliaryProgramException(String message) {
        super(message);
    }

    @Override
    public String getMessage() {
        return super.getMessage();
    }

    @Override
    public String toString() {
        return super.toString();
    }
}
